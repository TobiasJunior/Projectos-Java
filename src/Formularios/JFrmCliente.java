    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
       String bi = txtPesquisar.getText();
        String nome = txtPesquisar.getText();
        String contacto = txtPesquisar.getText();
        
        try {
            DefaultTableModel modeloDaTabela = (DefaultTableModel)tabelaCliente.getModel();
            modeloDaTabela.setNumRows(0);
            cl=MetodosDoCliente.listaDoCliente();
            for (Cliente cliente : cl) {
                if (cliente.getBi().equalsIgnoreCase(bi) || cliente.getNome().equalsIgnoreCase(nome)) {
                    modeloDaTabela.addRow(new Object [] {
                        cliente.getCodigo(),
                        cliente.getBi(),
                        cliente.getNome(),
                        cliente.getContacto()
                        
                    });
                }
                
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

