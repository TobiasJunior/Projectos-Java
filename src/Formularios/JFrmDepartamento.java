    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String codigo_departamento = txtPesquisar.getText();
        String nome_departamento = txtPesquisar.getText();
        
        try {
            DefaultTableModel modeloDaTabela = (DefaultTableModel)tabelaDep.getModel();
            modeloDaTabela.setNumRows(0);
            lista=MetodosDoDepartamento.listaDeDepartamento();
            for (Departamento departamento : lista) {
                if (departamento.getCodigo_departamento().equalsIgnoreCase(nome_departamento) || departamento.getNome_departamento().equalsIgnoreCase(nome_departamento)) {
                    modeloDaTabela.addRow(new Object [] {
                        departamento.getCodigo_departamento(),
                        departamento.getNome_departamento()
                        
                    });
                }
                
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

