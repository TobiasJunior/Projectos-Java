    public void listar (){
       DefaultTableModel modeloDaTabela= (DefaultTableModel)tabelaDep.getModel();
        modeloDaTabela.setNumRows(0);
        try{
            lista=MetodosDoDepartamento.listaDeDepartamento();
           for(Departamento depar: lista){
            modeloDaTabela.addRow(new Object[]{
                depar.getCodigo_departamento(),
                depar.getNome_departamento()
                       
            });
           
           }
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, "nao foi possivel listar");
        }
    }
