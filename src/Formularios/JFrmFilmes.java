    private void tabelaFilmeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaFilmeMouseClicked
       if(tabelaFilme.getSelectedRow()!=-1){
            int indice= tabelaFilme.getSelectedRow();
            TableModel modelo=tabelaFilme.getModel();
            txtCodigo.setText(modelo.getValueAt(indice,0).toString());
            txtNome.setText(modelo.getValueAt(indice,1).toString());
            txtGenero.setText(modelo.getValueAt(indice,2).toString());
            txtAno.setText(modelo.getValueAt(indice,3).toString());
            
            
        }
    }//GEN-LAST:event_tabelaFilmeMouseClicked

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String codigo = txtPesquisar.getText();
        String nome = txtPesquisar.getText();
        String genero = txtPesquisar.getText();
        String ano = txtPesquisar.getText();
        
        try {
            DefaultTableModel modeloDaTabela= (DefaultTableModel)tabelaFilme.getModel();
            modeloDaTabela.setNumRows(0);
            listaDeFilmes=MetodosDoFilme.listaDeFilme();
            for (Filmes filme : listaDeFilmes) {
                if (filme.getCodigo().equalsIgnoreCase(codigo)|| filme.getNome().equalsIgnoreCase(nome) || filme.getGenero().equalsIgnoreCase(genero) ||  filme.getAno().equalsIgnoreCase(ano)){
                    modeloDaTabela.addRow(new Object []{
                     filme.getCodigo(),
                     filme.getNome(),
                     filme.getGenero(),
                     filme.getAno(),
                     filme.getCodigoProdutora()
                     
                             });
                } 
                    
            } 
            
        } catch (IOException ex) {
           
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

